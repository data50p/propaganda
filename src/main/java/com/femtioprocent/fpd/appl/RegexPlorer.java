    Set<String> saved_re = new TreeSet<String>();
    Set<String> saved_txt = new TreeSet<String>();
    
    void restore() {
	ObjectHibernator<Set<String>> h1 = new ObjectHibernator<Set<String>>("re");
	Set<String> o1 = h1.restoreObject();
	if ( o1 != null )
	    saved_re = o1;
	ObjectHibernator<Set<String>> h2 = new ObjectHibernator<Set<String>>("txt");
	Set<String> o2 = h2.restoreObject();
	if ( o2 != null )
	    saved_txt = o2;
	
	ObjectHibernator<String> h3 = new ObjectHibernator<String>("note");
	String s3 = h3.restoreObject();
	if ( s3 != null )
	    re_note.setText(s3);

	updTxt();
	updRE();
    }
    
    void save() {
	ObjectHibernator<Set<String>> h1 = new ObjectHibernator<Set<String>>("re");
	h1.saveObject(saved_re);
	ObjectHibernator<Set<String>> h2 = new ObjectHibernator<Set<String>>("txt");
	h2.saveObject(saved_txt);
	ObjectHibernator<String> h3 = new ObjectHibernator<String>("note");
	h3.saveObject(re_note.getText());
    }
    
    private void saveRE() {
	try {
	    String re = (String)re_cb.getSelectedItem();
	    saved_re.add(re);
	    updRE();
	    re_cb.setSelectedItem(re);
	} catch (Exception ex) {
	}
	repaint();
    }

    private void saveTxt() {
	String txt = txt_cb.getSelectedItem().toString();
	saved_txt.add(txt);
	updTxt();
	txt_cb.setSelectedItem(txt);
    }
    
    void updTxt() {
	txt_cb.removeAllItems();
	for(String id : saved_txt ) {
	    txt_cb.addItem(id);
	}
    }

    void updRE() {
	re_cb.removeAllItems();
	for(String re2 : saved_re)
	    re_cb.addItem(re2);
    }
    
