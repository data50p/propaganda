    private void evalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_evalActionPerformed
	try {
	    JTextField[] cg = new JTextField[8+1];
	    cg[0] = cg0;
	    cg[1] = cg1;
	    cg[2] = cg2;
	    cg[3] = cg3;
	    cg[4] = cg4;
	    cg[5] = cg5;
	    cg[6] = cg6;
	    cg[7] = cg7;
	    cg[8] = cg8;
	    
	    JLabel[] lb = new JLabel[8+1];
	    //lb[0] = lb0;
	    lb[1] = lb1;
	    lb[2] = lb2;
	    lb[3] = lb3;
	    lb[4] = lb4;
	    lb[5] = lb5;
	    lb[6] = lb6;
	    lb[7] = lb7;
	    lb[8] = lb8;
	    for(int i = 1+0; i <= 8; i++) {
		lb[i].setEnabled(false);
	    }

    	    for(int i = 0; i <= 8; i++) {
		cg[i].setText("");
		cg[i].setEnabled(false);
	    }
	    cg0.setText("");
	    isMatched.setSelected(false);
	    isFound.setSelected(false);

	    
	    String re = re_cb.getSelectedItem().toString();
	    String text = txt_cb.getSelectedItem().toString();
	    Pattern pat = Pattern.compile(re);
	    Matcher mat = pat.matcher(text);
	    isMatched.setSelected(mat.matches());
	    mat.reset();
	    isFound.setSelected(mat.find());
	    isError.setSelected(false);
	    
	    
	    int gc = mat.groupCount();
	    for(int i = 0; i <= 8; i++) {
		if ( i > gc ) {
		}
		else {
		    cg[i].setText(mat.group(i));
		    cg[i].setEnabled(true);
		    if ( i > 0 )
			lb[i].setEnabled(true);
		}
	    }
	    cg0.setText(mat.group(0));
	    
	} catch (Exception ex) {
	    isError.setSelected(true);
	    
	}
    }//GEN-LAST:event_evalActionPerformed
    
